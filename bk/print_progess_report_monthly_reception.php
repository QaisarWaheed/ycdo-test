<?php
// OPTIMIZED: replaced per-row queries with pre-aggregated batch queries
include 'includes/connect.php';
require_once __DIR__ . '/includes/progress_report_params.php';

if (isset($_GET['date'])) {
    $date = $_GET['date'];
    $br_id = $_GET['br_id'];
} elseif (isset($_POST['date'])) {
    $date = $_POST['date'];
    $br_id = $_POST['br_id'];
} else {
    exit(0);
}

$br_id = (int) $br_id;
$date_esc = mysqli_real_escape_string($con, $date);
$month_prefix = $date_esc . '%';
$tok_date_clause = progress_sql_date_clause($con, $month_prefix);

$stats = array();
$aggregate_sql = "SELECT user_id, tokan_type_id, COUNT(*) AS token_count, COALESCE(SUM(cash), 0) AS cash_total
    FROM tokans
    WHERE branch_id = '$br_id'
    AND $tok_date_clause
    AND tokan_type_id IN ('1', '2', '3', '4')
    AND status = '1'
    GROUP BY user_id, tokan_type_id";
$run_aggregate = mysqli_query($con, $aggregate_sql);
if ($run_aggregate) {
    while ($row = mysqli_fetch_assoc($run_aggregate)) {
        $uid = (int) $row['user_id'];
        $type_id = (string) $row['tokan_type_id'];
        if (!isset($stats[$uid])) {
            $stats[$uid] = array(
                '1' => array('count' => 0, 'cash' => 0),
                '2' => array('count' => 0, 'cash' => 0),
                '3' => array('count' => 0, 'cash' => 0),
                '4' => array('count' => 0, 'cash' => 0),
            );
        }
        $stats[$uid][$type_id] = array(
            'count' => (int) $row['token_count'],
            'cash' => (float) $row['cash_total'],
        );
    }
}

$user_names = array();
if (count($stats) > 0) {
    $user_ids = implode(',', array_keys($stats));
    $name_sql = "SELECT id, u_name FROM users WHERE id IN ($user_ids)";
    $run_names = mysqli_query($con, $name_sql);
    if ($run_names) {
        while ($row = mysqli_fetch_assoc($run_names)) {
            $user_names[(int) $row['id']] = $row['u_name'];
        }
    }
}

ksort($stats);
