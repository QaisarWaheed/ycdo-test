<?php
$s = 0;
$total_poor = 0;
$total_member = 0;
$total_general = 0;
$total_emergency = 0;
$total_incentive = 0;

if (count($stats) > 0) {
    echo '<tbody>';
    foreach ($stats as $doctor => $types) {
        $s++;
        $doctor_name = $user_names[$doctor] ?? get_uname_by_id($doctor);
        $count_poor = $types['1']['count'];
        $poor = $types['1']['cash'];
        $count_member = $types['2']['count'];
        $member = $types['2']['cash'];
        $count_general = $types['3']['count'];
        $general = $types['3']['cash'];
        $count_emergency = $types['4']['count'];
        $emergency = $types['4']['cash'];

        $total_poor += $poor;
        $total_member += $member;
        $total_general += $general;
        $total_emergency += $emergency;

        $incentive = ($count_poor * 1) + ($count_member * 5) + ($count_general * 3);
        $total_incentive += $incentive;
        $row_total = $poor + $member + $general + $emergency;

        echo '<tr style="text-align: right;">';
        echo '<td>' . $s . '</td>';
        echo '<td>' . (int) $doctor . '</td>';
        echo '<td style="text-align: left;">' . htmlspecialchars($doctor_name, ENT_QUOTES, 'UTF-8') . '</td>';
        echo '<td>' . $count_poor . ' -> ' . $poor . '</td>';
        echo '<td>' . $count_general . ' -> ' . $general . '</td>';
        echo '<td>' . $count_member . ' -> ' . $member . '</td>';
        echo '<td>' . $count_emergency . ' -> ' . $emergency . '</td>';
        echo '<td>' . $row_total . '</td>';
        echo '<td>' . $incentive . '</td>';
        echo '</tr>';
    }
    echo '</tbody>';
    echo '<tfoot>
